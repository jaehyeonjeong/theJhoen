 * 
 * 이런 엔터티를 보다 상세히 표현하기 위해서는 속성이라는 하위요소들을 가진다.
 * 참고 : 주식별자르 pk(PRIMARY KEY)라고 한다.
 * 
 * 7. 다음 중 도서관 시스템 구축 시 작성 가능한 엔터티로 적합하지 않은 것은?
 * 1) 회원, 2) 대출, 3)도서, 4) 도서관
 * 답 2번, 아.. 4번이네... 도서관은 시스템 전체를 포함하는 한개의 객체이므로 엔터티가 될 수 없다.*/