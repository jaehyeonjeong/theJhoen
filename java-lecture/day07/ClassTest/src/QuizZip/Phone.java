package QuizZip;

public class Phone {
    private String name;
    private String tel;

    public Phone() {

    }

    public String getName() {
        return this.name;
    }

    public String getTel() {
        return this.tel;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
