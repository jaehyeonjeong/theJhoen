public class ComparisonOperator {
    public static void main(String[] args) {
        int age = 20;
        // 비교 연산자
        System.out.println(age > 20); //비교 연산자는 Boolean으로 떨어진다.
        System.out.println(age < 20);
        System.out.println(age >= 20);
        System.out.println(age <= 20);
        System.out.println(age == 20);
        System.out.println(age != 20);
        // 총 6개가 있음.
    }
}
